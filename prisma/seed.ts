// ---------------------------------------------------------------------------
// Purge helper — removes all deterministic seed records in FK-safe order
// ---------------------------------------------------------------------------

async function purgeSeedData() {
    console.log("[seed] Purging existing seed data...");

    // 1. Notifications referencing seeded users
    await prisma.notification.deleteMany({
        where: { userId: { in: Object.values(SEED_REF.users) } },
    });

    // Purge all data (order: notifications, goals, reports, templates, users, campuses, org groups)
    await prisma.notification.deleteMany({});
    await prisma.goal.deleteMany({});
    await prisma.reportEvent.deleteMany({});
    await prisma.reportMetric.deleteMany({});
    await prisma.reportSection.deleteMany({});
    await prisma.report.deleteMany({});
    await prisma.reportTemplateVersion.deleteMany({});
    await prisma.reportTemplate.deleteMany({});
    // Invite links reference users via `createdById` FK — remove them first.
    await prisma.inviteLink.deleteMany({});
    await prisma.user.deleteMany({});
    await prisma.campus.deleteMany({});
    await prisma.orgGroup.deleteMany({});
    await prisma.orgGroup.deleteMany({ where: { id: { in: Object.values(SEED_REF.groups) } } });

    console.log("[seed] ✓ Purge complete.");
}

