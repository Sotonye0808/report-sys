async function main() {
    if (RESET) {
        await purgeSeedData();
    }

    if (BASE_ONLY) {
        // Base-only seed: org structure + template (no demo users / reports)
        if (!RESET) {
            const existingGroup = await prisma.orgGroup.findFirst({ where: { name: "Nigeria Group" } });
            if (existingGroup) {
                console.log("[seed] Base data already seeded, skipping. Use --reset to overwrite.");
                return;
            }
        }

        const hashedPassword = await bcryptjs.hash("Nlp2026!", 12);
        await seedOrgStructure();
        // The superadmin user is required as the FK owner (createdById) on the
        // report template and template version — it must exist before seedTemplate().
        await seedSystemUser(hashedPassword);
        await seedTemplate();
        console.log("[seed] ✓ Base seed complete (groups, campuses, template).");
    } else {
        // Full seed: everything
        if (!RESET) {
            const existing = await prisma.user.findUnique({ where: { email: "superadmin@harvesters.org" } });
            if (existing) {
                console.log("[seed] Already seeded, skipping. Use --reset to overwrite.");
                return;
            }
        }

        const hashedPassword = await bcryptjs.hash("Nlp2026!", 12);
        await seedOrgStructure();
        await seedSystemUser(hashedPassword);
        await seedDemoUsers(hashedPassword);
        await seedTemplate();
        await seedReports();
        console.log("[seed] ✓ Seed complete");
    }
}

main()
    .catch((e) => {
        console.error("[seed] Error:", e);
        process.exit(1);
    })
    .finally(async () => {
        await prisma.$disconnect();
    });
