async function seedTemplate() {
    console.log("[seed] Creating report templates from config/reports.ts...");

    const templateConfigs = [DEFAULT_REPORT_TEMPLATE, WEEKLY_REPORT_TEMPLATE, MONTHLY_ONLY_REPORT_TEMPLATE];
    for (const config of templateConfigs) {
        console.log(`[seed]   → ${config.name}`);
        const template = await prisma.reportTemplate.create({
            data: {
                organisationId: ORG_ID,
                name: config.name,
                description: config.description,
                version: config.version,
                isActive: config.isActive,
                isDefault: config.isDefault,
                createdById: SEED_REF.users.superadmin.id,
            },
        });
        SEED_REF.templates[config.name] = template;
        for (const sec of config.sections) {
            const section = await prisma.reportTemplateSection.create({
                data: {
                    templateId: template.id,
                    name: sec.name,
                    description: sec.description,
                    order: sec.order,
                    isRequired: sec.isRequired,
                },
            });
            for (const met of sec.metrics) {
                await prisma.reportTemplateMetric.create({
                    data: {
                        sectionId: section.id,
                        name: met.name,
                        fieldType: met.fieldType,
                        calculationType: met.calculationType,
                        isRequired: met.isRequired,
                        order: met.order,
                        capturesGoal: met.capturesGoal,
                        capturesAchieved: met.capturesAchieved,
                        capturesYoY: met.capturesYoY,
                        description: met.description,
                    },
                });
            }
        }
        // Template version snapshot
        const fullTemplate = await prisma.reportTemplate.findUnique({
            where: { id: template.id },
            include: { sections: { include: { metrics: true }, orderBy: { order: "asc" } } },
        });
        const tplVersion = await prisma.reportTemplateVersion.create({
            data: {
                templateId: template.id,
                versionNumber: 1,
                snapshot: JSON.parse(JSON.stringify(fullTemplate)),
                createdById: SEED_REF.users.superadmin.id,
            },
        });
        SEED_REF.templateVersions[config.name] = tplVersion;
    }
}

